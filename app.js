//jshint esversion:6

const express = require("express");
const bodyParser = require("body-parser");
const ejs = require("ejs");
const mongoose = require('mongoose');

const homeStartingContent = "ðŸ‘‹ Hello, I'm Lucky, a dedicated Full Stack Flutter App Developer currently serving as a Android Developer at Swavish Software . With a deep passion for mobile app development and a strong expertise in Android and Flutter, I specialize in crafting robust and innovative solutions for businesses.";
const aboutContent = "With a comprehensive understanding of front-end development, back-end integration, and database management, I ensure the delivery of high-quality and scalable applications.";
const contactContent = "As a proactive learner, I stay updated with the latest advancements and best practices in mobile app development. I actively engage with the developer community, attend industry conferences, and contribute to open-source projects to broaden my knowledge and enhance my skill set.";

const app = express();

app.set('view engine', 'ejs');

app.use(bodyParser.urlencoded({extended: true}));
app.use(express.static("public"));

mongoose.connect("mongodb://localhost:27017/blogDB", {useNewUrlParser: true});

const postSchema = {
  title: String,
  content: String
};

const Post = mongoose.model("Post", postSchema);

app.get("/", function(req, res){

  Post.find({}, function(err, posts){
    res.render("home", {
      startingContent: homeStartingContent,
      posts: posts
      });
  });
});

app.get("/compose", function(req, res){
  res.render("compose");
});

app.post("/compose", function(req, res){
  const post = new Post({
    title: req.body.postTitle,
    content: req.body.postBody
  });


  post.save(function(err){
    if (!err){
        res.redirect("/");
    }
  });
});

app.get("/posts/:postId", function(req, res){

const requestedPostId = req.params.postId;

  Post.findOne({_id: requestedPostId}, function(err, post){
    res.render("post", {
      title: post.title,
      content: post.content
    });
  });

});

app.get("/about", function(req, res){
  res.render("about", {aboutContent: aboutContent});
});

app.get("/contact", function(req, res){
  res.render("contact", {contactContent: contactContent});
});


app.listen(3000, function() {
  console.log("Server started on port 3000");
});
